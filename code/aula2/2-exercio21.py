# 21.	Faça um algoritmo que leia uma temperatura em graus Celsius e apresente-a convertida em graus Fahrenheit.
# A fórmula de conversão é: F = (9 * C + 160) / 5, na qual F é a temperatura em Fahrenheit e C é a temperatura em Celsius;

# entrada
C = float(input("Digite a temperatura em Celsius:"))


# processamento
F = (9 * C + 160) / 5



# saida
print(F)